# For loops with List
List = [
    "apple", "banana", "orange", "grape", "strawberry", "blueberry", "pineapple", "mango",
    "pizza", "burger", "pasta", "salad", "sushi", "taco", "sandwich", "soup",
    "book", "car", "computer", "chair", "lamp", "phone", "keyboard", "mug"
]

for items in List:
    print(items)
print("\n\n")    

