# Logical

# Truth table of 'or'
H = True or True
print("True or True is",H)

J = True or False
print("True or False is",J)

K = False or True
print("False or True is",K)

L = False or False
print("False or False is",L)


# Truth table of 'and'

M = True and True
print("True and True is",M)

N = True and False
print("True and False is",N)

O = False and True
print("False and True is",O)

P = False and False
print("False and False is",P)

# Truth table of 'not'
Q = not True
print("not True is",Q)

R = not False
print("not False is",R)
