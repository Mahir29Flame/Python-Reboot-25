# Summing
num_1 = float(input("Enter a number: "))
num_2 = float(input("Enter a number: "))

print("The addition of the numbers are :",num_1 + num_2)
print("The subtraction of the numbers are :",num_1 - num_2)
print("The multiplication of the numbers are :",num_1 * num_2)
print("The division of the numbers are :",num_1 / num_2)